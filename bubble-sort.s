start:
MOV BYTE[0X00000], 0X05
MOV BYTE[0X00001], 0X03
MOV BYTE[0X00002], 0X02
MOV BYTE[0X00003], 0X04
MOV BYTE[0X00004], 0X08
MOV SI, 0X00000
MOV DI, 0X00010
MOV CL, 0X05
MOV CH, 0X06
DEC CH
JNZ SORT

INI:
MOV SI, DI
SUB SI, 0X04
ADD DI, 0X0000B
MOV CL, 0X05
DEC CH
JNZ SORT

SORT:
MOV AL, BYTE[SI]
INC SI
MOV DL, BYTE[DI]
CMP AL,DL
JB BEFORE
CMP AL,DL
JA AFTER
JMP NEXT

BEFORE:
MOV BYTE[DI], AL
INC DI
MOV BYTE[DI], DL
JMP NEXT

AFTER:
MOV BYTE[DI],DL
INC DI
MOV BYTE[DI], AL
JMP NEXT

NEXT:
DEC CL
JNZ SORT
DEC CH
JNZ INI

HLT