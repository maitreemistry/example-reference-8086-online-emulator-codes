VAL: DB 0X09
DB 0X04
LAST:DB 0X03

 

start:
MOV SI, OFFSET VAL
MOV AL, BYTE[SI] ; SMALL
MOV AH, BYTE[SI] ; GREAT
MOV CH, 0X03

 
FIND:
MOV BH, BYTE[SI]
CMP BH, AH
JA GRE
CMP BH, AL
JB SMA
INC SI
DEC CH
JNZ FIND
JMP OVER

GRE:
MOV AH,BH
INC SI
DEC CH
JNZ FIND

SMA:
MOV AL, BH
INC SI
DEC CH
JNZ FIND

OVER:
MOV BYTE[0X00050], AL
MOV BYTE[0X00060], AH

MOV AH, 0X002
MOV DL, BYTE[0X00050]
ADD DL, 0X0030
INT 0X0021

MOV AH, 0X002
MOV DL, BYTE[0X00060]
ADD DL, 0X0030
INT 0X0021

HLT